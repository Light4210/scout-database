    public function getGames(User|UserInterface $user)
    {
        if ($user->getUserPermision() == User::PRIORITY_NATIONAL_COUNCIL) {
            return $this->findBy([], ['status' => 'DESC']);
        } else {
            $qb = $this->createQueryBuilder('g');
            return $qb->where('g.status = :approved')->orWhere('g.author = :userId')->setParameter('userId', $user->getId())->setParameter('approved' , Game::STATUS_APPROVE)->getQuery()->execute();
        }
    }
